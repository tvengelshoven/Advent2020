Forest_Length = len(Forest[0])
Latitude = 0
Tree_Counter = 0
Count = []

for Rate in Rates:
    for Longitude,Trees in enumerate(Forest):
        if is_multiple(Longitude,Rate[1]):
            if Trees[Latitude] == '#':
                Tree_Counter += 1
            Latitude += Rate[0]
            Latitude = Forest_Generator(Latitude)
    print("Tree Encounters for {} right and {} down:".format(*Rate),Tree_Counter)
    Count.append(Tree_Counter)
    Tree_Counter = 0
    Latitude = 0
print("Product of all Trees:",Product(Count))

