def Forest_Generator(Latitude):
    if Latitude <= Forest_Length - 1:
        return Latitude
    else:
        return Latitude - Forest_Length

def is_multiple(Value,Denominator):
    Quotient = str(Value / Denominator)
    Decimal = int(Quotient.split('.')[1])
    if Decimal == 0:
        return True
    else: return False

def Product(List):
    X = 1
    for Value in List:
        X = X * Value
    return X


